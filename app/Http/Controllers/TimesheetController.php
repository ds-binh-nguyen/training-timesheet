    {
        $params = $request->only([
            'time_check_in',
            'time_check_out',
            'difficult',
            'planning',
            'tasks',
        ]);
        $params['user_id'] = auth()->user()->id;
        $this->timesheetService->createTimesheet($params);

        return view('timesheets.index');
    }

